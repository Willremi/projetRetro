                <h3>Résumé</h3>
                <p>Cette série relate les aventures du Docteur, un extraterrestre de la race des Seigneurs du Temps (Time Lord) originaire de la planète Gallifrey. Dans la première série, coupé de son peuple, il voyage à bord d'un TARDIS (Time And Relative Dimension(s) In Space, ou Temps à relativité Dimensionnelle inter Spatiale en français), une machine pouvant voyager dans l'espace et dans le temps. Particulièrement attaché à la Terre, il est régulièrement accompagné dans ses voyages par des compagnons, pour la plupart humains.
