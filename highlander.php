    <h2>Annecdotes</h2>
          <ul class="annecdote" type="circle">
            <li>Highlander est l’adaptation télévisée de la franchise éponyme sur grand écran. Le premier film a été réalisé par Russell Mulcahy en 1986, avec Christophe Lambert et Sean Connery. Quatre suites ont vu le jour au cinéma : Highlander le retour (1990), Highlander III (1994), Highlander : Endgame (2000) et Highlander : The Source (2007). Si Christophe Lambert, dans le rôle de Connor MacLeod, a été la vedette des quatre premiers opus, Adrian Paul a pris le relais au cours d’Highlander : Endgame, avec de revenir seul en tête d’affiche en 2007.</li>
            <br>
            <li>Outre Christophe Lambert, de nombreuses personnalités ou vedettes en devenir sont apparues dans la série. Parmi elles : Marion Cotillard, Franck Dubosc, Tonya Kinzinger, Lorànt Deutsch, Tomer Sisley, Ron Perlman, Astrid Veillon, Alice Evans ou Claire Keim.</li>
            <br>
            <li>Au fil des saisons, l’accroche du générique a connu plusieurs modifications. À partir de la saison 4, le texte était : « Son nom : Duncan MacLeod ; on l’appelle « Highlander ». Il est né en 1592 dans les Hautes Terres d’Écosse et il est toujours de ce monde. Il est immortel. Depuis 400 ans sa vie est un combat, une romance, une quête. Sans relâche, il affronte d’autres immortels dans un duel à mort où le vainqueur décapite l’adversaire et s’approprie toute sa puissance. Je suis un guetteur. J’appartiens à une société secrète qui a pour but d’observer et de consigner leurs faits et gestes sans jamais interférer. Nous savons tout sur les immortels. À la fin, un seul d’entre eux survivra. Ce sera peut-être Duncan MacLeod : Highlander! »</li>
            <br>
            <li>À l’origine, le personnage d’Adrian Paul devait être Connor MacLeod, le héros du film. Finalement, l’acteur a souhaité créer son propre univers. L’idée d’un nouveau MacLeod a été retenue.</li>
            <br>
            <li> La ville de Seacouver où vit le héros est en réalité la contraction entre Seattle et Vancouver. En effet, le tournage a eu lieu dans la ville canadienne, tandis qu’il était supposé avoir lieu sur la côte Ouest américaine.</li>
            <br>
            <li>Avant de rejoindre le casting international d’Highlander, Alexandra Vandernoot était apparue dans Le Retour d’Arsène Lupin (1988) ou Les Compagnons de l’aventure (1990). Après son départ de la série, en raison de sa grossesse, l’actrice a rejoint les plateaux de la télévision française. Elle a notamment été la vedette de Tramontane (1999), Le Bleu de l’océan (2003), Ondes de choc (2007) ou À tort et à raison (à partir de 2009).</li>
            <br>
            <li>Plusieurs autres dérivés de la franchise ont vu le jour : la série L’Immortelle à la fin des années 1990, ainsi que le dessin animé Highlander, produit entre 1994 et 1996. Ils ont été découverts sur M6.</li>
            <br>
            <li>Highlander a été programmée pour la première fois en France le mercredi 1er septembre 1993 sur TF1. La série était alors diffusée à 13h35, avant Le Club Dorothée. Avant elle, 21 Jump street, Alerte à Malibu ou Flash avait occupé cette case.</li>
            <br>
            <li>L'acteur Stan Kirsch est retrouvé mort à son domicile le 11 janvier après s'être suicidé par pendaison</li>
